# Data type: Integer

x = 10

puts x
puts x.class # => Integer

# Math operations
x = x + 10 # => 20
# Shorthand
# x += 10

x = x - 5 # => 15
# Shorthand
# x -= 5

x = x * 4 # => 60
# Shorthand
# x *= 4

x = x / 2 # => 30
# Shorthand
# x /= 2

x = x % 7 # => 2
# Shorthand
# x %= 7

x = 10 / 3 # => 3

# Convert integer to float
x.to_f # => 3.0

# Convert integer to string
x.to_s # => "3"